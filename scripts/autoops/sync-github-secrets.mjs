#!/usr/bin/env node
import crypto from "node:crypto";
import { discoverCoolifyAppUuid } from "./lib/coolify.mjs";
import { discoverVolcInstances } from "./lib/volc-openapi.mjs";
import { buildGhSecretCommands, syncSecretsWithGh } from "./lib/github.mjs";
import { autoopsDefaults, loadLocalEnvFiles, logJson, parseArgs, writeRuntimeJson, writeRuntimeText } from "./lib/logger.mjs";

function generatedSecret() {
  return crypto.randomBytes(32).toString("base64url");
}

async function main() {
  await loadLocalEnvFiles();
  const args = parseArgs();
  const dryRun = Boolean(args.dryRun);
  const repo = args.repo || autoopsDefaults().repo;

  const generatedAlertRouterSecret = !process.env.AUTOOPS_ALERT_ROUTER_SECRET;
  if (generatedAlertRouterSecret) {
    process.env.AUTOOPS_ALERT_ROUTER_SECRET = generatedSecret();
  }
  if (!process.env.COOLIFY_APP_UUID) {
    const discovered = await discoverCoolifyAppUuid({ dryRun });
    if (discovered.uuid) {
      process.env.COOLIFY_APP_UUID = discovered.uuid;
    }
  }
  if (!process.env.VOLC_ECS_INSTANCE_IDS) {
    const discovered = await discoverVolcInstances({ dryRun });
    if (discovered.instanceIds?.length === 1) {
      process.env.VOLC_ECS_INSTANCE_IDS = discovered.instanceIds.join(",");
    }
  }

  const requiredNames = [
    "VOLC_AK",
    "VOLC_SK",
    "VOLC_REGION",
    "GITHUB_TOKEN",
    "COOLIFY_API_KEY",
    "COOLIFY_BASE_URL",
    "AUTOOPS_ALERT_ROUTER_SECRET",
    "COOLIFY_APP_UUID",
    "VOLC_ECS_INSTANCE_IDS",
  ];
  const optionalNames = ["OPENAI_API_KEY"];
  const secretMap = Object.fromEntries([...requiredNames, ...optionalNames].map((name) => [name, process.env[name] || ""]));
  const present = Object.keys(secretMap).filter((name) => secretMap[name]);
  const missing = requiredNames.filter((name) => !secretMap[name]);
  const result = syncSecretsWithGh(secretMap, { repo, dryRun });
  const commands = result.commands?.length ? result.commands : buildGhSecretCommands(present, repo);
  if (generatedAlertRouterSecret) {
    await writeRuntimeText(
      "generated-secrets.local.env",
      [
        "# Gitignored local handoff values generated by pnpm autoops:sync-secrets.",
        "# Do not commit or paste these values into chat.",
        `AUTOOPS_ALERT_ROUTER_SECRET=${process.env.AUTOOPS_ALERT_ROUTER_SECRET}`,
        process.env.COOLIFY_APP_UUID ? `COOLIFY_APP_UUID=${process.env.COOLIFY_APP_UUID}` : "",
        process.env.VOLC_ECS_INSTANCE_IDS ? `VOLC_ECS_INSTANCE_IDS=${process.env.VOLC_ECS_INSTANCE_IDS}` : "",
        "",
      ]
        .filter((line) => line !== "")
        .join("\n")
    );
  }
  await writeRuntimeJson("github-secrets-sync.json", {
    repo,
    present,
    missing,
    synced: result.synced || [],
    commands,
    openai_api_key_present: Boolean(secretMap.OPENAI_API_KEY),
    generated_alert_router_secret: generatedAlertRouterSecret,
  });
  logJson("github.secrets.sync.completed", {
    repo,
    present,
    missing,
    synced: result.synced || [],
    fallback_commands_count: commands.length,
    openai_api_key_present: Boolean(secretMap.OPENAI_API_KEY),
    generated_alert_router_secret: generatedAlertRouterSecret,
  });
  if (missing.length && !dryRun) {
    process.exitCode = 2;
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
